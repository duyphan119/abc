  CREATE OR REPLACE EDITIONABLE TRIGGER "CREATE_ORDER_TRIGGER" after insert on orders 
for each row
begin
    FOR c_record IN (select ci.product_detail_id_pk ,p.new_price as price,ci.quantity   
        from cart_items ci, product_details p_d, products p ,carts c 
        where ci.product_detail_id_pk = p_d.product_detail_id  
        and p_d.product_id_pk = p.product_id and c.cart_id = ci.cart_id_pk
        and c.account_id_pk = :new.account_id_pk)
    LOOP
        insert into order_items(order_id_pk, product_detail_id_pk,price, quantity) values 
        (:new.order_id, c_record.product_detail_id_pk, c_record.price, c_record.quantity);
    END LOOP; 
    delete from cart_items where cart_id_pk = (select cart_id from carts where account_id_pk = :new.account_id_pk FETCH FIRST 1 ROWS ONLY);
end;
