--  DDL for Trigger UPDATE_INVENTORY_TRIGGER
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE TRIGGER "UPDATE_INVENTORY_TRIGGER" after insert or update or delete on order_items
FOR EACH ROW
begin 
    IF INSERTING THEN
        update product_details set inventory = inventory - :new.quantity 
            where product_detail_id = :new.product_detail_id_pk;
    elsif DELETING Then
        update product_details set inventory = inventory + :old.quantity 
            where product_detail_id = :old.product_detail_id_pk;
    elsif UPDATING Then
        update product_details set inventory = inventory + :old.quantity - :new.quantity 
            where product_detail_id = :new.product_detail_id_pk;
    else 
        update product_details set inventory = 0;
    end if;
end;
