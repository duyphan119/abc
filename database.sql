  CREATE OR REPLACE EDITIONABLE TRIGGER "CREATE_CART_ITEM_TRIGGER" before insert on cart_items
FOR EACH ROW
DECLARE
    e_count number;
    e_item_id number;
    new_quantity NUMBER;
begin
select count(item_id) into e_count
        from cart_items
        where cart_id_pk = :new.cart_id_pk and product_detail_id_pk = :new.product_detail_id_pk;
        if e_count > 0 then begin
     select item_id into e_item_id
        from cart_items
        where cart_id_pk = :new.cart_id_pk and product_detail_id_pk = :new.product_detail_id_pk;
        select (quantity + :new.quantity) into new_quantity
        from cart_items
        where cart_id_pk = :new.cart_id_pk and product_detail_id_pk = :new.product_detail_id_pk;
    if e_item_id is not null then begin
        if new_quantity <= get_inventory_function(:new.product_detail_id_pk) then
        begin
        delete from cart_items where cart_id_pk = :new.cart_id_pk and product_detail_id_pk = :new.product_detail_id_pk;
        :new.quantity := new_quantity;
        :new.item_id :=e_item_id;
        end;
        else begin
        raise_application_error(-20001
                , 'Out of stock');
        end;
        end if;
    end; end if;
    end; end if;
end;
